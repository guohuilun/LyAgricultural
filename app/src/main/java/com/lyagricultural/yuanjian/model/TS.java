public class TS
{
	private int tsp;
	private int trp;
	private int tcp;
	private String tip;
	public int getTsp()
	{
		return tsp;
	}
	public void setTsp(int tsp)
	{
		this.tsp = tsp;
	}
	public int getTrp()
	{
		return trp;
	}
	public void setTrp(int trp)
	{
		this.trp = trp;
	}
	public int getTcp()
	{
		return tcp;
	}
	public void setTcp(int tcp)
	{
		this.tcp = tcp;
	}
	public String getTip()
	{
		return tip;
	}
	public void setTip(String tip)
	{
		this.tip = tip;
	}
}